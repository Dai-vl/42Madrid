char	*ft_strcpy(char *dest, char *src)
{
	char	*p1;
	char	*p2;

	p1 = src;
	p2 = dest;
	while (*p1 != '\0')
	{
		*p2 = *p1;
		++p2;
		++p1;
	}
	*p2 = '\0';
	return (dest);
}
