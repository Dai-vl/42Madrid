#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*ptr;

	i = 0;
	ptr = lst;
	while (ptr->next)
	{
		++i;
		ptr = ptr->next;
	}	
	return (i);
}