char	*ft_toupper(char *str)
{
	char	*aux;

	aux = str;
	while (*aux != '\0')
	{
		if (*aux >= 'a' && *aux <= 'z')
		{
			*aux -= 32;
		}
		++aux;
	}
	return (str);
}
