void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	aux;
	int	*cp;

	i = 0;
	cp = tab + size - 1;
	while (i < size / 2)
	{
		aux = *tab;
		*tab = *cp;
		*cp = aux;
		--cp;
		++tab;
		++i;
	}
	tab -= size / 2;
}
