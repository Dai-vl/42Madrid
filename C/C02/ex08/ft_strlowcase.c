char	*ft_strlowcase(char *str)
{
	char	*aux;

	aux = str;
	while (*aux != '\0')
	{
		if (*aux >= 'A' && *aux <= 'Z')
		{
			*aux += 32;
		}
		++aux;
	}
	return (str);
}
