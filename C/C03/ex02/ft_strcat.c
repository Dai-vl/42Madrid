char	*ft_strcat(char *dest, char *src)
{
	char	*ps;
	char	*pd;

	ps = src;
	pd = dest;
	while (*pd != '\0')
	{
		++pd;
	}
	while (*ps != '\0')
	{
		*pd = *ps;
		++pd;
		++ps;
	}
	*pd = '\0';
	return (dest);
}
